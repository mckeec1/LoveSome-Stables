<div class="content" id="s">
    <h2 id="job">Side Walker Job Description</h2>
    <p>Side walker’s physically assists the rider (during riding, mounting and dismounting), helps the rider guide their
        horse during the lesson, and assists the rider in following the directions of the instructor as necessary.
        The amount of assistant is dependent on each rider’s abilities. Horse experience is not necessary. The side
        walker reports to the Lesson Instructor and Program Director.</p>
    <br>
    <h3 id="listTitle">Qualifications:</h3>
    <ul id="list">
        <li class="listItem"> - Commitment to attend your scheduled night for 6 weeks.</li>
        <li class="listItem"> - Walk and occasionally jog along side the horse for the duration of the lesson, at times on uneven surfaces.</li>
        <li class="listItem"> - Ability to hold your arm slightly raised and out to the side for up to 30 minutes providing support to the rider as necessary.</li>
        <li class="listItem"> - Must have adequate vision to ensure the safety of rider.</li>
        <li class="listItem"> - Ability to adapt to change, be flexible and patient.</li>
        <li class="listItem"> - Comfortable working with children and/or adults with disabilities and special needs and with horses (including physical contact).</li>
        <li class="listItem"> - Minimum age of 14 years.</li>
        <li class="listItem"> - Cannot be fearful of horses.</li>
    </ul>

    <h3 id="listTitle">Responsibilities:</h3>
    <ul id="list">
        <li class="listItem"> - Meet and greet riders. </li>
        <li class="listItem"> - Remain with your rider ensuring their safety at all times.</li>
        <li class="listItem"> - Assist parent or rider with correct fitting helmet</li>
        <li class="listItem"> - Assist with supporting the rider during mounting/dismounting.</li>
        <li class="listItem"> - Bridge” the instructor to the rider. (Repeat directions, redirect attention, cheer, encourage and praise)</li>
    </ul>
