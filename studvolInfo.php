<div class="content" id="s">
    <h2>Lesson Information</h2>
    <p>Lesson are held on Monday evenings, Wednesday evenings and Saturday mornings.  The cost will be $25 per lesson.
        Payment is due for entire session on or before the first lesson.</p>

    <br>

    <p>Our riding sessions will have one to four riders similar in either age or ability, and will focus on increasing
        individual riding skills while gaining therapeutic benefits. These benefits include increased muscle tone and
        strength, improving eye/hand coordination and balance, improving peer interaction and communication, increasing
        self-esteem, and developing a sense of control. Instructors establish goals for each rider, working with the parents,
        guardian and /or rider, at the beginning of each session. They create a wide variety of games and exercises designed
        to increase the student’s ability to listen, learn and communicate. In addition, all riders work on improving
        their horsemanship skills and a willingness to try new things. Students not only improve riding skills but are
        taught how to groom and tack their horses. The lessons will last approximately 45 minutes.</p>
</div>

