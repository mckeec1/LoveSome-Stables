<div class="content" id="s">
    <h2 id="job">Horse Leader Job Description</h2>
    <p>Horse leader’s primary responsibility is to the horse, i.e. prepare the horse for class, maintain safety spacing,
        and help the horse follow the directions of the rider during class. The leader must be at least 14 years old with
        horse experience. The horse leader reports to Lesson Instructor and Program Director.</p>
    <br>
    <h3 id="listTitle">Qualifications:</h3>
    <ul id="list">
        <li class="listItem"> - Commitment to attend your schedule lesson night for 6 weeks.</li>
        <li class="listItem"> - Must have a strong understanding and experience with horse handling.</li>
        <li class="listItem"> - Minimum age of 14 years.</li>
        <li class="listItem"> - Walk and occasionally jog with the horse for the duration of the lesson with occasional jogging on uneven surfaces.</li>
        <li class="listItem"> - Must have adequate vision and hearing to ensure safety of horses and participants.</li>
        <li class="listItem"> - Ability to “read” a horse and the knowledge and confidence to calmly prevent unexpected movements.</li>
        <li class="listItem"> - Ability to adapt to change, be flexible and patient.</li>
    </ul>

    <h3 id="listTitle">Responsibilities:</h3>
    <ul id="list">
        <li class="listItem"> - Must remain focused on the horse at all times during the lesson to ensure safety, claming the horse and anticipating any unforeseen, situation.</li>
        <li class="listItem"> - Ensure horse is groomed, tacked and warming up in arena 10 minutes prior to lesson start time.</li>
        <li class="listItem"> - Lead the horse in class. Give guidance to the horse from the rider’s cues as necessary.</li>
        <li class="listItem"> - Listen to the directions of the instructor.</li>
        <li class="listItem"> - Untack & turn out the horse after class (unless it is needed in the class following.)</li>
    </ul>

</div>

