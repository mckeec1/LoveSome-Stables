        Equine facilitated learning program for disabled veterans

        Summer farm camp for youth and adults with disabilities

        Organic garden

        Work training site for young adults with disabilities</p>
    <br>

    <h2>Why Therapeutic Riding?</h2>

    <br>

    <p>The benefits of therapeutic horseback riding are unique.
        The gentle rhythmical gait of the horse replicates a muscle movement pattern similar to the human walk.
        This stimulates the vestibular, nervous and circulatory systems.</p>

    <br>

    <p>Physically, it helps strengthen balance, coordination, mobility, and muscle tone. It also helps with motor control, range of motion, sensory integration, and visual/spatial orientation.
        Cognitively, it increases the ability to focus and stay on task, communication, judgment, follow directions, problem solving skills and sequencing.
        Socially, it helps with cooperation, empathy, responsibility, respect and trust.
        Emotionally, it helps build confidence, motivation, goal setting and self-esteem, self –confidence, and trust through the horse/rider bond
        Behaviorally, it helps with relaxation and self-discipline</p>

